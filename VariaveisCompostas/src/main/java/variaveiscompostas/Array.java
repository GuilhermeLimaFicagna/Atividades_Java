package variaveiscompostas;
public class Array {
    public static void main(String[] args) {
        //teste de vertor
        int n[] = {1, 2, 3, 8, 6};
        for (int c = 0; c < n.length; c+=1 ) {
            System.out.println(n[c]);
        }
        System.out.println("=-=-===-");
        System.out.println(n.length);
    }
}
